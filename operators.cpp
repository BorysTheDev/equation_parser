#include "operators.h"

namespace ops
{
double neg(double val)
{
    return -val;
}

double add(double larg, double rarg)
{
    return larg + rarg;
}

double sub(double larg, double rarg)
{
    return larg - rarg;
}

double div(double larg, double rarg)
{
    return larg / rarg;
}

double mul(double larg, double rarg)
{
    return larg * rarg;
}




}
